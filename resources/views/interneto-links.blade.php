    <h1 id="interneto-links">Interneto links</h1>
    <p>Interneto links is a free collection / directory / OSINT (open source intelligence) of categorized webs (around 33k webs).
        The directory is structured in !Interneto (info folder) + 13 main categories with subcategories inside where are accordingly stored the links. These links are homepage sites like <a href="https://subdomain.domain.tld/">https://subdomain.domain.tld/</a>. Also there are two specific categories: one for content of websites (audios, books, news) saved in the &quot;Content&quot; and public users of platforms (content creators) in &quot;Channels &amp; Profiles&quot;. Besides there are hashtags to facilitate the search.</p>
    <p><strong>Interneto</strong> = <strong>In</strong> + <strong>Ter</strong> + <strong>Net</strong> + <strong>O</strong> = <strong>In</strong>side of the <strong>Ter</strong>a (1 trillion) networks circular closed connections (<strong>O</strong>)</p>
    <p>Links hosted in <a href="https://raindrop.io/Interneto">raindrop.io/interneto</a></p>
    <ul>
        <li><strong>In</strong>: inside</li>
        <li><strong>Ter</strong>: tera = <img src="https://render.githubusercontent.com/render/math?math=1%20\cdot%2010^{12}" alt="formula"></li>
        <li><strong>Net</strong>: networks</li>
        <li><strong>O</strong>: circular closed connections</li>
    </ul>
    <h2 id="explanation">Explanation</h2>
    <p>This project came from the idea of getting a free accessible categorized database of all internet sites. Right now (2021/12/22) there are 1,914,441,355 webs however less than 200 million are active, around 80% of websites are inactive by parked domains or similar.
        Data from:</p>
    <ul>
        <li><a href="https://www.internetlivestats.com/total-number-of-websites/">Total number of websites</a></li>
        <li><a href="https://news.netcraft.com/archives/category/web-server-survey/">Web Server Survey - Netcraft News</a></li>
        <li><a href="https://majestic.com/reports/majestic-million">Majestic million</a>, first million domains with the most referring subnets.</li>
    </ul>
    <p>Categorize 200 million dynamic websites that born and die is almost impossible but the main idea is to create a big list of functional and relevant webs in a classification system. Two main project have committed to do this work in the past: <a href="http://dmoz-odp.org">DMOZ (Directory of Mozilla)</a> and <a href="http://Curlie.org">Curlie (Collector of URLs)</a>, however DMOZ closed in 2017 and Curlie.org is very inactive.</p>
    <p>In addition, nowadays with the improvement of search engines the web directories have been abandoned because of the trending of search engines with quick answers like Google, DuckDuckGo, Bing, Yandex... But web directories are still great alternatives to search engines for various reasons:</p>
    <ul>
        <li>Does not consume energy of the servers.</li>
        <li>Does not use your privacy to target with ads and extract information.</li>
        <li>Have a big list of webs about the same topic.</li>
        <li>It is not sorted by marketing, ads or SEO position instead of search engines do. </li>
        <li>It is totally free as always you have a local database of links.</li>
        <li>You can download all the links and manage locally. </li>
    </ul>
    <p>The main issue about web directories is the way to search information, its hierarchical structure with categories and subcategories difficult the process of searching. Therefore, all efforts should be focus towards improving the quality of the directory structure. For that reason is necessary some tool that permit to categorize, organize and display content in a good way.</p>
    <p>The most popular search engines in terms of proportions. The spider represents the web crawler, the AI bot that index the Web.</p>
    <ul>
        <li>Google: 92.2% head of the spider controls the web</li>
        <li>Bing (also Yahoo!): 3.77% from Microsoft one the biggest corporation</li>
        <li>Baidu: 1.45% a big Chinese internet company</li>
        <li>Yandex: 0.68% a big Russian internet company</li>
        <li>At the bottom are represented the search engines that cares for your privacy, like DuckDuckGo (0.59%), Startpage, Qwant, PrivacyWall, YaCy (DAPP search engine). If you want to use the power of the spider you have to sell your soul.</li>
    </ul>
