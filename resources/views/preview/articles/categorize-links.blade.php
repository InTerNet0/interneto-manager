    <h2>
        How to categorize links?
    </h2>

    <h3>Problem with classification</h3>
    <p>For example Google has topics in many sections like: +Net, Arts, Business, Education, Media, Society, World. How do we categorize the Google webs, by the platform, the owner company, its use or the topic? </p>
    <p>The general &quot;Google&quot; folder is inside &quot;Business – Enterprises – Internet – Communication&quot;, 4th steps inside the parent folder. If we organize by topics, the webs would be moved to different folders:</p>
    <ul>
        <li><strong>Google blog</strong> in Media – Blogs Media – Software Blogs</li>
        <li><strong>Google maps</strong> in World – Maps – Earth</li>
        <li><strong>YouTube</strong> in +Net - Content – Video - Video Platform</li>
    </ul>
    <p>But if we organize by company name, it won&#39;t be a section categorized by topic or platform. To solve this problem arises the <em>Tags</em>, to group links by another topic and maintaining the folder structure, such us:</p>
    <ul>
        <li>#Google, #Maps, #Video-platform</li>
    </ul>
    <p>For me the best way to classify webs is to organize by topic or use, and then add the correspondent tags like #Company: Google
        And if you want to reuse a link it would be good to create a shortcut with the path folder or group by tags.</p>
    <p>The ways of sorting can be: </p>
    <ul>
        <li>2D either with columns, keys, circles, lists, trees. </li>
        <li>3D (with the use of shadows and colors).</li>
        <li>Sorting by:<ul>
                <li>alphabetical order</li>
                <li>color used</li>
                <li>date of creation</li>
                <li>loading speed</li>
                <li>number of words</li>
                <li>number of visits</li>
                <li>region</li>
                <li>score</li>
                <li>server consumption / server type</li>
                <li>similarity between logos</li>
                <li>storage</li>
                <li>subject
                    &quot;If a mind analyzes all possible parameters occurring in a day the size of the mind to process it would be one light year in diameter.&quot;
                </li>
            </ul>
        </li>
    </ul>

    <h4>The way to classify can vary</h4>
    <ul>
        <li>the most common is to classify from general to the concrete, from the most abstract to the most concise.</li>
        <li>since the internet contains everything imaginable and more, it becomes a dictionary classification.</li>
        <li>within the classification there are many methods to organize the content and therefore labeling is necessary in addition to a hierarchical tree.</li>
    </ul>

