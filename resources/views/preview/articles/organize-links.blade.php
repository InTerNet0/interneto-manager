    <h1 id="how-to-organize-or-categorize-links">How to organize or categorize links</h1>
    <ul>
        <li>How to join related links in a database. Example all the social networks of a person belong to different platforms but are part of one person. In raindrop.io you can only link them by creating them within the same folder or adding them within the same hashtag.</li>
        <li>It is better to categorize them in folders by platform than by person, since there are infinitely more people than platforms.</li>
    </ul>
    <h2 id="how-to-categorize-content-into-folders-">How to categorize content into folders:</h2>
    <ul>
        <li>According to their author, their platform, their professional dedication, the type of content.
            Classify the links or files inside the folders</li>
        <li>According to their date of creation, their author, title</li>
        <li>Have the database locally</li>
    </ul>
    <h2 id="how">How</h2>
    <ul>
        <li>If you organize it as a bookmark
